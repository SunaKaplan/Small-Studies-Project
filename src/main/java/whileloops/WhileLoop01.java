package whileloops;

public class WhileLoop01 {
    public static void main(String[] args) {

        // 5 ile 10 arasindaki(5 ve 10 dahiol) sayilari ayni satirda yazdirin

        for (int i = 5; i <= 10; i++) {
            System.out.println(i + " ");

        }

        System.out.print("\n**********************");

        int i = 5;

        while (i <= 10) {
            System.out.print(i + " ");


            i++;
        }
    }
    }


